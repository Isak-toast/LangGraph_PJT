"""
tools.py - 에이전트 도구 정의
==============================

이 파일은 에이전트들이 사용할 수 있는 도구(Tools)를 정의합니다.
도구는 에이전트가 외부 세계와 상호작용하는 방법입니다.

현재 도구:
- Tavily Search: 웹 검색 API (Researcher 에이전트가 사용)

도구란?
- LLM이 직접 할 수 없는 작업을 수행하는 함수
- 예: 웹 검색, 데이터베이스 조회, API 호출, 계산 등
"""

import os
from langchain_community.tools.tavily_search import TavilySearchResults


# ================================================================
# Tavily Search Tool (웹 검색 도구)
# ================================================================
# 
# Tavily는 AI 에이전트를 위해 최적화된 검색 API입니다.
# 일반 검색 엔진과 달리 AI가 처리하기 좋은 형태로 결과를 반환합니다.
#
# 사용 방법:
#   result = tavily_tool.invoke("검색할 쿼리")
#
# 반환 형식:
#   [{"content": "검색 결과 내용...", "url": "https://..."}, ...]
#
# 환경 변수 필요:
#   TAVILY_API_KEY=tvly-xxxxxxxxxxxx

tavily_tool = TavilySearchResults(
    max_results=3  # 최대 검색 결과 수 (비용 절감을 위해 3개로 제한)
)


# ================================================================
# 사용 가능한 도구 목록
# ================================================================
# 
# 새 도구를 추가하려면:
# 1. 도구 객체 생성 (위 tavily_tool처럼)
# 2. 이 리스트에 추가
# 3. 해당 도구를 사용할 에이전트에 연결 (nodes.py에서)

tools = [tavily_tool]
